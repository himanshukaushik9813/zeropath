/**
 * gen-test-vector.mjs
 *
 * Emits Soroban-encoded bytes for a REAL, snarkjs-verified Groth16 proof
 * (demo leaf 0) as Rust array literals for the on-chain verifier integration
 * test (contracts/stellar/settlement/src/test.rs).
 *
 * Usage: node gen-test-vector.mjs
 */

import { generateDemoProof } from "./proofgen.mjs";

function rustArray(name, hex) {
  const byteLen = hex.length / 2;
  const bytes = [];
  for (let i = 0; i < hex.length; i += 2) bytes.push(`0x${hex.slice(i, i + 2)}`);
  let out = `const ${name}: [u8; ${byteLen}] = [\n`;
  for (let i = 0; i < bytes.length; i += 12) {
    out += `    ${bytes.slice(i, i + 12).join(", ")},\n`;
  }
  out += "];\n";
  return out;
}

async function main() {
  console.error("Generating Groth16 proof (this takes a few seconds)...");
  const { sorobanEncoded, publicInputsHex, publicSignals, epoch } = await generateDemoProof(0);
  console.error("snarkjs.groth16.verify: OK");
  console.error("publicSignals [batch_root, source_event_root, nullifier_hash, destination_commitment, asset_id, epoch]:");
  console.error(publicSignals);

  const piNames = [
    "PI_BATCH_ROOT",
    "PI_SOURCE_EVENT_ROOT",
    "PI_NULLIFIER_HASH",
    "PI_DESTINATION_COMMITMENT",
    "PI_ASSET_ID",
    "PI_EPOCH",
  ];

  let rust = "// ===== AUTO-GENERATED by circuits/gen-test-vector.mjs — real Groth16 proof for demo leaf 0 =====\n";
  rust += "// snarkjs.groth16.verify against build/verification_key.json passed before emission.\n\n";
  rust += rustArray("PROOF_A", sorobanEncoded.a) + "\n";
  rust += rustArray("PROOF_B", sorobanEncoded.b) + "\n";
  rust += rustArray("PROOF_C", sorobanEncoded.c) + "\n";
  for (let i = 0; i < 6; i++) rust += rustArray(piNames[i], publicInputsHex[i]) + "\n";
  rust += `// epoch as u64 = ${epoch}\n`;

  console.log(rust);
}

main().then(() => process.exit(0)).catch((e) => {
  console.error(e);
  process.exit(1);
});
