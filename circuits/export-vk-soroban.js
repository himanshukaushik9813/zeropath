/**
 * export-vk-soroban.js
 *
 * Reads verification_key.json (snarkjs format) and outputs Rust constants
 * for hardcoding into the Soroban settlement contract.
 *
 * Soroban BN254 encoding:
 *   G1 (64 bytes): be_bytes(x) || be_bytes(y)
 *   G2 (128 bytes): be_bytes(x.c1) || be_bytes(x.c0) || be_bytes(y.c1) || be_bytes(y.c0)
 *   Fr (32 bytes): big-endian scalar
 *
 * snarkjs format:
 *   G1: [x, y, "1"]  (projective, but x/y are affine when z=1)
 *   G2: [[x.c0, x.c1], [y.c0, y.c1], ["1","0"]]
 */

import { readFileSync, writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));

function fieldToBeBytes32(decimalString) {
  const n = BigInt(decimalString);
  const hex = n.toString(16).padStart(64, "0");
  return hex;
}

function encodeG1(point) {
  // point = [x, y, "1"]
  const x = fieldToBeBytes32(point[0]);
  const y = fieldToBeBytes32(point[1]);
  return x + y; // 128 hex chars = 64 bytes
}

function encodeG2(point) {
  // point = [[x.c0, x.c1], [y.c0, y.c1], ["1","0"]]
  // Soroban expects: be(x.c1) || be(x.c0) || be(y.c1) || be(y.c0)
  const x_c0 = fieldToBeBytes32(point[0][0]);
  const x_c1 = fieldToBeBytes32(point[0][1]);
  const y_c0 = fieldToBeBytes32(point[1][0]);
  const y_c1 = fieldToBeBytes32(point[1][1]);
  return x_c1 + x_c0 + y_c1 + y_c0; // 256 hex chars = 128 bytes
}

function hexToRustArray(hex, name, byteLen) {
  const bytes = [];
  for (let i = 0; i < hex.length; i += 2) {
    bytes.push(`0x${hex.slice(i, i + 2)}`);
  }

  // Format as Rust const
  let rust = `const ${name}: [u8; ${byteLen}] = [\n`;
  for (let i = 0; i < bytes.length; i += 16) {
    rust += `    ${bytes.slice(i, i + 16).join(", ")},\n`;
  }
  rust += "];\n";
  return rust;
}

function main() {
  const vkPath = join(__dirname, "build", "verification_key.json");
  let vk;
  try {
    vk = JSON.parse(readFileSync(vkPath, "utf-8"));
  } catch {
    console.error(`ERROR: Cannot read ${vkPath}`);
    console.error("Run 'bash build.sh' first to compile the circuit and generate the verification key.");
    process.exit(1);
  }

  console.log("=== Verification Key Summary ===");
  console.log(`  Protocol: ${vk.protocol}`);
  console.log(`  Curve: ${vk.curve}`);
  console.log(`  nPublic: ${vk.nPublic}`);
  console.log(`  IC points: ${vk.IC.length}`);
  console.log("");

  // Encode all VK components
  const alphaHex = encodeG1(vk.vk_alpha_1);
  const betaHex = encodeG2(vk.vk_beta_2);
  const gammaHex = encodeG2(vk.vk_gamma_2);
  const deltaHex = encodeG2(vk.vk_delta_2);

  let rustCode = "// Auto-generated by export-vk-soroban.js — do not edit manually.\n";
  rustCode += `// Circuit: private_settlement, nPublic: ${vk.nPublic}\n\n`;

  rustCode += hexToRustArray(alphaHex, "VK_ALPHA_G1", 64);
  rustCode += "\n";
  rustCode += hexToRustArray(betaHex, "VK_BETA_G2", 128);
  rustCode += "\n";
  rustCode += hexToRustArray(gammaHex, "VK_GAMMA_G2", 128);
  rustCode += "\n";
  rustCode += hexToRustArray(deltaHex, "VK_DELTA_G2", 128);
  rustCode += "\n";

  // IC points (nPublic + 1 G1 points)
  rustCode += `const VK_IC_LEN: usize = ${vk.IC.length};\n\n`;

  for (let i = 0; i < vk.IC.length; i++) {
    const icHex = encodeG1(vk.IC[i]);
    rustCode += hexToRustArray(icHex, `VK_IC_${i}`, 64);
    rustCode += "\n";
  }

  // Write output
  const outPath = join(__dirname, "build", "vk_constants.rs");
  writeFileSync(outPath, rustCode);
  console.log(`Rust constants written to: ${outPath}`);
  console.log("Copy into contracts/stellar/settlement/src/lib.rs");

  // Also output as JSON for SDK/frontend use
  const vkJson = {
    alpha: alphaHex,
    beta: betaHex,
    gamma: gammaHex,
    delta: deltaHex,
    ic: vk.IC.map((point) => encodeG1(point)),
    nPublic: vk.nPublic,
  };

  const jsonOutPath = join(__dirname, "build", "vk_soroban.json");
  writeFileSync(jsonOutPath, JSON.stringify(vkJson, null, 2));
  console.log(`JSON encoding written to: ${jsonOutPath}`);
}

main();
