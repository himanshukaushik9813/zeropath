#[contract]
pub struct ZeroPathSettlement;

#[contractimpl]
impl ZeroPathSettlement {
    /// Initialize the contract. Called once after deployment.
    pub fn initialize(env: Env, admin: Address, relayer: Address) {
        if env.storage().instance().has(&DataKey::Admin) {
            panic!("already initialized");
        }
        env.storage().instance().set(&DataKey::Admin, &admin);
        env.storage().instance().set(&DataKey::Relayer, &relayer);
        env.storage().instance().set(&DataKey::Paused, &false);
    }

    /// Relayer submits a verified source-chain Merkle root.
    pub fn update_source_root(env: Env, relayer: Address, root: BytesN<32>) {
        relayer.require_auth();
        let expected: Address = env.storage().instance().get(&DataKey::Relayer).unwrap();
        if relayer != expected {
            panic!("not relayer");
        }
        env.storage()
            .persistent()
            .set(&DataKey::VerifiedRoot(root.clone()), &true);
        env.events().publish((Symbol::new(&env, "root"),), root);
    }

    /// Settle a private intent by verifying a Groth16 BN254 proof on-chain.
    pub fn settle(env: Env, intent: SettlementIntent, proof: Groth16Proof) {
        Self::assert_live(&env);

        // 1. Nullifier must not have been spent
        if env
            .storage()
            .persistent()
            .has(&DataKey::Nullifier(intent.nullifier_hash.clone()))
        {
            panic!("nullifier spent");
        }

        // 2. Source event root must have been submitted by the relayer
        if !env
            .storage()
            .persistent()
            .has(&DataKey::VerifiedRoot(intent.source_event_root.clone()))
        {
            panic!("unverified source root");
        }

        // 3. Build public inputs vector from intent fields
        let public_inputs = Self::build_public_inputs(&env, &intent);

        // 4. Verify Groth16 BN254 proof using CAP-0074 host functions
        if !Self::verify_groth16(&env, &proof, &public_inputs) {
            panic!("invalid proof");
        }

        // 5. Mark nullifier as spent (replay protection)
        env.storage()
            .persistent()
            .set(&DataKey::Nullifier(intent.nullifier_hash.clone()), &true);

        // 6. Record liquidity for epoch accounting
        Self::record_liquidity(&env, intent.epoch, intent.asset_id.clone(), intent.amount_bucket);

        // 7. Transfer tokens to recipient
        let client = token::Client::new(&env, &intent.token);
        client.transfer(
            &env.current_contract_address(),
            &intent.recipient,
            &intent.amount_bucket,
        );

        // 8. Emit settlement event
        env.events().publish(
            (Symbol::new(&env, "settled"), intent.epoch),
            (intent.nullifier_hash, intent.destination_commitment),
        );
    }

    pub fn pause(env: Env, admin: Address) {
        admin.require_auth();
        Self::require_admin(&env, &admin);
        env.storage().instance().set(&DataKey::Paused, &true);
    }

    pub fn unpause(env: Env, admin: Address) {
        admin.require_auth();
        Self::require_admin(&env, &admin);
        env.storage().instance().set(&DataKey::Paused, &false);
    }

    // -----------------------------------------------------------------------
    // Groth16 BN254 verification (CAP-0074)
    // -----------------------------------------------------------------------

    /// Verify a Groth16 proof against the hardcoded verification key.
    ///
    /// The Groth16 verification equation is:
    ///   e(A, B) == e(alpha, beta) * e(vk_x, gamma) * e(C, delta)
    ///
    /// Rearranged for pairing_check (product of pairings == 1):
    ///   e(-A, B) * e(alpha, beta) * e(vk_x, gamma) * e(C, delta) == 1
    fn verify_groth16(env: &Env, proof: &Groth16Proof, public_inputs: &[Bn254Fr; N_PUBLIC]) -> bool {
        let bn254 = env.crypto().bn254();

        // Load hardcoded VK points
        let vk_alpha = Bn254G1Affine::from_array(env, &VK_ALPHA_G1);
        let vk_beta = Bn254G2Affine::from_array(env, &VK_BETA_G2);
        let vk_gamma = Bn254G2Affine::from_array(env, &VK_GAMMA_G2);
        let vk_delta = Bn254G2Affine::from_array(env, &VK_DELTA_G2);

        // Load IC points
        let ic: [Bn254G1Affine; N_PUBLIC + 1] = [
            Bn254G1Affine::from_array(env, &VK_IC_0),
            Bn254G1Affine::from_array(env, &VK_IC_1),
            Bn254G1Affine::from_array(env, &VK_IC_2),
            Bn254G1Affine::from_array(env, &VK_IC_3),
            Bn254G1Affine::from_array(env, &VK_IC_4),
            Bn254G1Affine::from_array(env, &VK_IC_5),
            Bn254G1Affine::from_array(env, &VK_IC_6),
        ];

        // Compute vk_x = IC[0] + sum(public_inputs[i] * IC[i+1])
        let mut vk_x = ic[0].clone();
        for i in 0..N_PUBLIC {
            let term = bn254.g1_mul(&ic[i + 1], &public_inputs[i]);
            vk_x = bn254.g1_add(&vk_x, &term);
        }

        // Negate proof.a (flip Y coordinate: y -> field_modulus - y)
        let neg_a = Self::negate_g1(env, &proof.a);

        // Parse proof points
        let proof_b = Bn254G2Affine::from_bytes(proof.b.clone());
        let proof_c = Bn254G1Affine::from_bytes(proof.c.clone());

        // Build pairing check vectors:
        // e(-A, B) * e(alpha, beta) * e(vk_x, gamma) * e(C, delta) == 1
        let g1_points = Vec::from_array(env, [neg_a, vk_alpha, vk_x, proof_c]);
        let g2_points = Vec::from_array(env, [proof_b, vk_beta, vk_gamma, vk_delta]);

        bn254.pairing_check(g1_points, g2_points)
    }

    /// Negate a G1 point by computing (x, p - y) where p is the BN254 base field prime.
    pub(crate) fn negate_g1(env: &Env, point: &BytesN<64>) -> Bn254G1Affine {
        let point_bytes = point.to_array();

        // Extract x (first 32 bytes) and y (last 32 bytes)
        let mut x_bytes = [0u8; 32];
        let mut y_bytes = [0u8; 32];
        x_bytes.copy_from_slice(&point_bytes[..32]);
        y_bytes.copy_from_slice(&point_bytes[32..]);

        // Compute p - y (big-endian subtraction)
        let mut neg_y = [0u8; 32];
        let mut borrow: u16 = 0;
        for i in (0..32).rev() {
            let diff = (BN254_FIELD_MODULUS[i] as u16)
                .wrapping_sub(y_bytes[i] as u16)
                .wrapping_sub(borrow);
            neg_y[i] = diff as u8;
            borrow = if diff > 0xFF { 1 } else { 0 };
        }

        // Reconstruct G1 point with negated Y
        let mut result = [0u8; 64];
        result[..32].copy_from_slice(&x_bytes);
        result[32..].copy_from_slice(&neg_y);
        Bn254G1Affine::from_array(env, &result)
    }

    /// Build the 6-element public inputs array from a SettlementIntent.
    /// Order must match the circuit's public input declaration:
    ///   [batch_root, source_event_root, nullifier_hash, destination_commitment, asset_id, epoch]
    fn build_public_inputs(env: &Env, intent: &SettlementIntent) -> [Bn254Fr; N_PUBLIC] {
        // Convert epoch (u64) to a 32-byte big-endian field element
        let mut epoch_bytes = [0u8; 32];
        let epoch_be = intent.epoch.to_be_bytes();
        epoch_bytes[24..32].copy_from_slice(&epoch_be);

        [
            Bn254Fr::from_bytes(intent.batch_root.clone()),
            Bn254Fr::from_bytes(intent.source_event_root.clone()),
            Bn254Fr::from_bytes(intent.nullifier_hash.clone()),
            Bn254Fr::from_bytes(intent.destination_commitment.clone()),
            Bn254Fr::from_bytes(intent.asset_id.clone()),
            Bn254Fr::from_bytes(BytesN::from_array(env, &epoch_bytes)),
        ]
    }

    // -----------------------------------------------------------------------
    // Helpers
    // -----------------------------------------------------------------------

    fn record_liquidity(env: &Env, epoch: u64, asset_id: BytesN<32>, amount: i128) {
        let key = DataKey::EpochLiquidity(epoch, asset_id);
        let current: i128 = env.storage().persistent().get(&key).unwrap_or(0);
        env.storage().persistent().set(&key, &(current + amount));
    }

    fn assert_live(env: &Env) {
        let paused: bool = env
            .storage()
            .instance()
            .get(&DataKey::Paused)
            .unwrap_or(false);
        if paused {
            panic!("paused");
        }
    }

    fn require_admin(env: &Env, admin: &Address) {
        let expected: Address = env.storage().instance().get(&DataKey::Admin).unwrap();
        if admin != &expected {
            panic!("not admin");
        }
    }
}

